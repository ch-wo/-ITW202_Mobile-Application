package edu.gcit.todo_9;

public class Calculator {
    public double adding(double value1, double value2){
        return value1 + value2;
    }

    public double subtracting(double value1, double value2){
        return value1 - value2;
    }

    public double multiplying(double value1, double value2){
        return value1 * value2;
    }

    public double dividing(double value1, double value2){
        return value1 / value2;
    }
}
